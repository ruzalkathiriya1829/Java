
// with return type without argument

public class FunctionWithReturnTypeWithoutArgument {

    void areaofsquare() {

        int a = 5;
        int result;
        result = (a * a);
        System.out.println("area of the square= " + result);
    }

    public static void main(String[] args) {

        FunctionWithReturnTypeWithoutArgument F = new FunctionWithReturnTypeWithoutArgument();
        F.areaofsquare();

    }
}