public class CheckNumberEvenOrOdd_5 {
    
    public static void main(String[] args) {
        
        int number = 73;

        if(number % 2 != 0)
        {
            System.out.println(number + " is Odd number");
        }
        else
        {
            System.out.println(number + " is Even number");;
        }

    }

}
