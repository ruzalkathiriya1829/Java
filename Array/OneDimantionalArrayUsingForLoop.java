public class OneDimantionalArrayUsingForLoop {

    public static void main(String[] args) {
        
        int a[] = new int [5];

        a[0] = 10;
        a[1] = 33;
        a[2] = 45;
        a[3] = 55;
        a[4] = 90;

        for(int i=0; i<5; i++)
        {
            System.out.println(a[i]);
        }
    }
    
}
